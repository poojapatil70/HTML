        //Integer
        int number = 10; //capacity 10 digits
        System.out.println(number);

        short num = 11111;  //capacity 5 digits
        System.out.println(num);

        byte n = 42; //three number capacity
        System.out.println(n);

        long longNumber = 7972677714775607513l;//cpacity is 10 but after applying l at last total 20 number we can add
        System.out.println(longNumber);

        short shortNumber = 7777; //capacity of 4 number
        System.out.println(shortNumber);

